UNIVERSES = {
    "starter": STARTER_UNIVERSE,
    "large_cap": LARGE_CAP_UNIVERSE,
}


def get_universe(name: str) -> list[str]:
    try:
        return UNIVERSES[name]
    except KeyError as exc:
        choices = ", ".join(sorted(UNIVERSES))
        raise ValueError(f"Unknown universe '{name}'. Choices: {choices}") from exc
